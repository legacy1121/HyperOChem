			<p class='subtitle'>Introduction</p>
			<p>A <i>Substitution Reaction</i> is a reaction in which one substituent group is exchanged for another. It can occur when a suitable electrophile is treated with a nucleophile. The electrophile in a substitution reaction is known as the <i>substrate</i>. Haloalkane, Alkyl Halide, and Organohalide are all names for a halogen (that can easily act as a leaving group) which is attached to an alkane; it is classified as primary, secondary, or tertiary based on number of substituents.</p>
			<hr>
			<p class='subtitle'>The S<sub>N</sub>2 Mechanism</p>
			<p>The S<sub>N</sub>2 reaction involves two molecules colliding in a single reaction step, so can be classified as <i>bimolecular</i> (indicated by the 2 in the name). In many cases there is an <i>inversion of configuration</i>, meaning the halogen on the molecule being attacked repels the nucleophile and causes it to “attack” from the opposite side, leading to a flip in the stereochemical configuration from reactant to product. The S<sub>N</sub>2 reaction is also characterized by occurring in one step so contains a non-isolatable transition state. The process is illustrated below:</p>
