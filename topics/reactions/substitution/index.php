			<p class='subtitle'>Determining Mechanisms and Selecting Reagents</p>
			<p>Sulfonate ions are the most commonly used leaving groups along with halides, and a Tosylate Group (Ts) is the most commonly used sulfonate ion. When an OH<sup>-</sup> ion is available as a leaving group, it can be replaced with an OTs<sup>-</sup> to make it more likely to separate. A <i>protic solvent</i> contains at least one hydrogen atom connected directly to an electronegative atom; they stabilize cations and anions with lones pairs and H-bonding respectively. A <i>polar aprotic solvent</i> contains no hydrogen atoms connected directly to an electronegative atom; it will stabilize cations, but not anions because it is polar enough to attack the positively charged ion, but has no hydrogens available to neutralize the negatively charged ion.</p>
