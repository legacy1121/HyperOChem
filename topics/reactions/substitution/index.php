			<p class='subtitle'>The S<sub>N</sub>1 Mechanism</p>
			<p>The S<sub>N</sub>1 Mechanism is <i>unimolecular</i>, a single molecule undergoing two steps and therefore has a <i>Rate-Determining Step</i>. This is the step with the highest energy of activation, so takes the longest to occur and dictates the speed of the overall reaction. In the S<sub>N</sub>1 mechanism, two products are formed, one product maintains its configuration (<i>retention of configuration</i>) and the other “flips” like in an S<sub>N</sub>2. Enantiomers will only occur if there is a chiral center.</p>
