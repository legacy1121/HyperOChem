			<p class='subtitle'>Newman Projections</p>
			<p>Newman projections are a useful tool to help assist with alkane stereochemistry. They allow for an easy method to view the molecule as a whole in order to analyze R and S configuration as well as they show different arrangements such as a “staggered” or “eclipsed” structure.</p>
			<img src='newmanprojection.png' width = 20% height = 26%/>
			<p>The above picture shows the difference in a staggered conformation and an eclipsed conformation. Typically the staggered conformation is preferred because the molecule is more spread out or evenly spaced and that reduces repulsion between the attachments on the first or front carbon and the attachments on the second or back carbon. The reason multiple configurations are possible is because alkanes are comprised of single bonds which are able to rotate.</p>
