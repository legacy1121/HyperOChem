			<p>
			Isomers are two molecules with the same formula but different structure. There are two different types of Isomers (Constitutional isomers and Stereoisomers):
					<li>Constitutional Isomers
						<ul>
							<li>Same molecular formula but different constitution (order of connectivity of atoms)</li>
							<img src="constitutionalisomersdotjpeg.png" width = 30% height = 17% />
						</ul>
					</li>
					<li>Stereoisomers
						<ul>
							<li>Same molecular formula and constitution but molecules have different shape and arrangement</li>
							<img src="stereoisomersdotjpeg.png" width=30% height=25%/>
						</ul>
					</li>
					<li>Understanding the Difference in R and S Configuration
						<ol>
							<li>Compare the atomic numbers of the molecules. The highest atomic number receives the highest priority.</li>
							<li>Arrange the molecule with the lowest priority facing away from you.</li>
							<li>Find the order of the priorities and determine if number increase going clockwise or counterclockwise.</li>
							<li>If clockwise, the configuration is R. If counterclockwise, the configuration is S.</li>
						</ol>
					</li>
